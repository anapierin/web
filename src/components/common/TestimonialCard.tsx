export interface TestimonialCardProps {
  name: string
  text: string
  rating: number
  className?: string
}

export default function TestimonialCard({ 
  name, 
  text, 
  rating, 
  className = '' 
}: TestimonialCardProps) {
  const getInitials = (fullName: string) => {
    return fullName
      .split(' ')
      .map(word => word.charAt(0))
      .join('')
      .substring(0, 2)
      .toUpperCase()
  }

  return (
    <div className={`card fade-in space-y-6 ${className}`}>
      {/* Star Rating */}
      <div className="flex space-x-1">
        {[1, 2, 3, 4, 5].map((star) => (
          <svg
            key={star}
            className={`w-5 h-5 ${
              star <= rating ? 'text-yellow-400' : 'text-gray-300'
            }`}
            fill="currentColor"
            viewBox="0 0 20 20"
          >
            <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
          </svg>
        ))}
      </div>

      {/* Testimonial Text */}
      <blockquote className="text-text-primary text-lg leading-relaxed italic">
        &ldquo;{text}&rdquo;
      </blockquote>

      {/* Author */}
      <div className="flex items-center space-x-4">
        <div className="w-14 h-14 bg-brand-600 rounded-full flex items-center justify-center text-white font-semibold">
          {getInitials(name)}
        </div>
        <div>
          <div className="font-semibold text-text-primary">{name}</div>
          <div className="text-text-muted text-sm">Paciente</div>
        </div>
      </div>
    </div>
  )
}